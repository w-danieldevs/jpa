    private void findBySex(){
        String selectSex = jComboBoxSex.getSelectedItem().toString();
		List<Pet> listPets = myCoordinator.queryPetSex(selectSex);
		if (listPets.size() > 0) {
			fillBoard(listPets);
		} else {
			JOptionPane.showMessageDialog(null, "No hay mascotas con sexo " + selectSex);
		}
    }
    
    private void findAll(){
        List<Pet> listPets = myCoordinator.queryListPet(null);
        
	if (listPets.size() > 0) {
            fillBoard(listPets);

	} else {
            JOptionPane.showMessageDialog(null, "No hay mascotas ");
	}
    }
    
    private void fillBoard(List<Pet> listPets){
        
        String columnsName [] = { "Identificacion", "Nombre", "Sexo", "Color", "Raza" };

		String info[][] = new String[listPets.size()][5];

		for (int x = 0; x < listPets.size(); x++) {
			info[x][0] = String.valueOf(listPets.get(x).getPetId());
			info[x][1] = listPets.get(x).getName();
			info[x][2] = listPets.get(x).getBreed();
			info[x][3] = listPets.get(x).getColor();
			info[x][4] = listPets.get(x).getSex();
		}

		petBoard = new JTable(info, columnsName);
		scrollBoard.setViewportView(petBoard);

		int[] anchos = { 50, 120, 120, 120, 80 };
		for (int i = 0; i < petBoard.getColumnCount(); i++) {
			petBoard.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
		}
    }
    
    
    
